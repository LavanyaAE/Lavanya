class ToothPaste 
{ 
    String brand;
    String flavor;
    int volume;
    String color;
    boolean hasFluoride;
    boolean isWhitening;
    boolean isSensitive;
    double price;

  
ToothPaste(String brand, String flavor, int volume, String color,boolean hasFluoride, boolean isWhitening, boolean isSensitive, double price) 
{
        this.brand = brand;
        this.flavor = flavor;
        this.volume = volume;
        this.color = color;
        this.hasFluoride = hasFluoride;
        this.isWhitening = isWhitening;
        this.isSensitive = isSensitive;
        this.price = price;
}

    
}