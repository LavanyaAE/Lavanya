class Pot 
 {
   
     String material;
     String shape;
     int capacity;
     String color;
     int durability;
     double price;
     double weight;
     double height;

   
Pot(String material, String shape, int capacity, String color,int durability,double price,double weight,double height)
{
        this.material = material;
        this.shape = shape;
        this.capacity = capacity;
        this.color = color;
        this.durability=durability;
        this.price = price;
        this.weight=weight;
        this.height=height;
}
}