class Stove 
{ 
    String brand;
    String fuelType;
    int numberOfBurners;
    boolean hasOven;
    boolean hasGrill;
    boolean isElectric;
    String color;
    double price;

    
Stove(String brand, String fuelType, int numberOfBurners, boolean hasOven,boolean hasGrill, boolean isElectric, String color, double price) 
{
        this.brand = brand;
        this.fuelType = fuelType;
        this.numberOfBurners = numberOfBurners;
        this.hasOven = hasOven;
        this.hasGrill = hasGrill;
        this.isElectric = isElectric;
        this.color = color;
        this.price = price;
}
}
    
    